#!/usr/bin/env node
//
// One-time migration: copy every sponsored CAMPAIGN post — both ContentPost
// and SocialPost — and their daily analytics snapshots into the unified
// Content entity (source=sponsored). This is Phase 1.5 of the content-model
// unification, a sibling of scripts/migrate-blogs-to-content.mjs.
//
// ADDITIVE + IDEMPOTENT. This script NEVER deletes or mutates the original
// ContentPost/SocialPost rows (or their *Snapshot children). It only writes
// NEW Content rows (and their STATS children) that can coexist with the
// campaign post rows during the dual-read transition.
//
// ---------------------------------------------------------------------------
// THE GLOBAL-POOL TENANT WRINKLE — READ THIS BEFORE RUNNING
// ---------------------------------------------------------------------------
// Campaign posts live under pk = CAMPAIGN#{campaignId}: a GLOBAL pool with NO
// tenantId on the campaign, the vendor, or the post rows. The Content entity,
// by contrast, REQUIRES pk = TENANT#{sub}. There is simply no data on these
// rows from which the owning tenant could be derived.
//
// Resolution (already decided — implemented exactly): this script takes a
// REQUIRED `--tenant <sub>` flag naming the Cognito sub that owns these
// sponsored posts. EVERY migrated Content row is written under that tenant's
// partition. The script REFUSES to run (exit 1) if `--tenant` is missing. It
// does NOT try to infer the tenant from the data — there isn't any.
// ---------------------------------------------------------------------------
//
// Usage:
//   AWS_PROFILE=staging node scripts/migrate-campaign-posts-to-content.mjs \
//       --table content-tracking --tenant 4f9c...sub
//   AWS_PROFILE=prod    node scripts/migrate-campaign-posts-to-content.mjs \
//       --table content-tracking --tenant 4f9c...sub --apply
//
// Without `--apply` the script runs in dry-run mode (the default): it prints
// what WOULD be written but writes nothing. Run twice — once to confirm the
// counts look right, then again with --apply.
//
// Idempotency: contentId is derived deterministically from the postId (with a
// kind prefix — see below), and the Content root + STATS children are written
// with a plain PutItem keyed on the Content sk (an overwrite). We deliberately
// do NOT call domain createContent/putStatsSnapshot (createContent guards with
// attribute_not_exists(sk) and stamps "now"); instead we replicate their exact
// key/attribute shape with a low-level Put. Re-running is therefore safe and
// simply rewrites the same rows in place.
//
// postId collision analysis: postIds are ULIDs from ulid(). ContentPost and
// SocialPost are generated by the SAME ulid() namespace, so while a collision
// across the two kinds is astronomically unlikely, nothing structurally
// forbids it (and the original rows are distinguished by their sk prefix, not
// by id). To guarantee the two kinds can never clobber each other's Content
// row, contentId is prefixed by kind: "cp_{postId}" for ContentPost and
// "sp_{postId}" for SocialPost. The prefix is deterministic, so re-runs still
// overwrite the same row.
//
// Why a Scan: this is an operator script that must cover ALL campaigns. The
// per-campaign list helpers (listContentPosts/listSocialPosts) don't sweep the
// table, so the lowest-risk approach is a one-shot paginated ScanCommand
// filtered to the four campaign-post entities — exactly the idiom established
// by migrate-blogs-to-content.mjs. A one-shot Scan is acceptable precisely
// because this runs once.

import { DynamoDBClient, paginateScan } from "@aws-sdk/client-dynamodb";
import { unmarshall } from "@aws-sdk/util-dynamodb";

const args = parseArgs(process.argv.slice(2));
if (args.help || !args.table || !args.tenant) {
  console.error(
    "Usage: migrate-campaign-posts-to-content.mjs --table <name> --tenant <sub> [--apply] [--region us-east-1]\n" +
      "\n" +
      "  --table   DynamoDB single-table name (required)\n" +
      "  --tenant  Cognito sub that owns these sponsored posts (required —\n" +
      "            campaign posts carry no tenantId, so the owning tenant\n" +
      "            cannot be inferred and MUST be supplied)\n" +
      "  --apply   perform the writes (default is dry-run)\n" +
      "  --region  AWS region (default us-east-1)\n" +
      "  --help    show this help",
  );
  process.exit(args.help ? 0 : 1);
}

// services/ddb.mjs reads TABLE_NAME from env at import time, and the domain
// modules import it at module load, so set it BEFORE the dynamic import below.
process.env.TABLE_NAME = args.table;

const { PutCommand } = await import("@aws-sdk/lib-dynamodb");
const { ddb, TABLE_NAME } = await import("../api/services/ddb.mjs");
const { contentKey, statsKey } = await import("../api/domain/content.mjs");
const { CONTENT_TYPES, CONTENT_SOURCES } = await import("../api/validation/content.mjs");

// ContentPost rows whose platform isn't a recognizable video platform default
// to this type (see contentPostType below for the full rationale). Declared
// before the startup assertions that reference it (module-top code runs in
// order — a later const would be in its temporal dead zone here).
const DEFAULT_CONTENT_POST_TYPE = "blog";
const VIDEO_PLATFORMS = new Set(["youtube", "youtube_shorts", "tiktok", "vimeo", "twitch"]);

// Fail fast if the target enums ever drift away from the constants this
// migration writes. Keeps the migration honest against validation/content.mjs.
if (!CONTENT_SOURCES.includes("sponsored")) {
  throw new Error(`CONTENT_SOURCES no longer includes "sponsored": ${CONTENT_SOURCES.join(", ")}`);
}
if (!CONTENT_TYPES.includes("social")) {
  throw new Error(`CONTENT_TYPES no longer includes "social": ${CONTENT_TYPES.join(", ")}`);
}
// ContentPost rows are mapped to type DEFAULT_CONTENT_POST_TYPE (see below).
if (!CONTENT_TYPES.includes(DEFAULT_CONTENT_POST_TYPE)) {
  throw new Error(
    `CONTENT_TYPES no longer includes "${DEFAULT_CONTENT_POST_TYPE}": ${CONTENT_TYPES.join(", ")}`,
  );
}

// ---------------------------------------------------------------------
// Pure transforms — exported near the top so they COULD be unit-tested in
// isolation (there are no jest tests for scripts in this repo, so none is
// added here). All transforms take the post's owning tenant explicitly since
// the campaign rows carry no tenantId of their own.
// ---------------------------------------------------------------------

// type mapping:
//   SocialPost  -> "social"  (always — the entity IS a social post)
//   ContentPost -> derived from its `platform` (the only type-bearing field a
//                  ContentPost carries; there is no separate content-type
//                  attribute). Video platforms map to "video"; everything else
//                  (and absence of a recognizable platform) defaults to "blog".
//
// Why default "blog": ContentPost models a long-form/article-style placement
// (the campaign's "content" deliverable, as opposed to a SocialPost). Of the
// valid CONTENT_TYPES ("blog", "social", "video"), "blog" is the closest match
// for a generic article/written placement, and "social" is reserved for the
// SocialPost kind. So an unrecognized ContentPost platform lands on "blog".
// (DEFAULT_CONTENT_POST_TYPE + VIDEO_PLATFORMS are declared above, before the
// startup assertions that reference DEFAULT_CONTENT_POST_TYPE.)
function contentPostType(post) {
  const platform = typeof post.platform === "string" ? post.platform.toLowerCase() : "";
  if (VIDEO_PLATFORMS.has(platform)) return "video";
  return DEFAULT_CONTENT_POST_TYPE;
}

// kind -> contentId prefix, so a ContentPost and a SocialPost can never clobber
// each other's Content row even in the (astronomically unlikely) event their
// ULIDs collide.
function contentIdFor(kind, postId) {
  return kind === "SocialPost" ? `sp_${postId}` : `cp_${postId}`;
}

// ---------------------------------------------------------------------
// Pure transform: campaign post root (ContentPost | SocialPost) -> Content root.
//
// Deterministic: contentId = "{cp|sp}_{postId}", createdAt/updatedAt preserved
// (NOT stamped "now"), GSI1 keys set so the row shows up in listContentByTenant.
// Mirrors the exact attribute shape domain createContent produces.
// ---------------------------------------------------------------------
export function postToContentItem(post, kind, tenantId) {
  const contentId = contentIdFor(kind, post.postId);
  const createdAt = post.createdAt;
  const updatedAt = post.updatedAt ?? post.createdAt;
  const type = kind === "SocialPost" ? "social" : contentPostType(post);

  const item = {
    ...contentKey(tenantId, contentId),
    entity: "Content",
    tenantId,
    contentId,
    type, // always a valid CONTENT_TYPES value (asserted at startup)
    source: "sponsored", // CONTENT_SOURCES includes "sponsored" (asserted at startup)
    // The migrated sponsored content links back to its originating campaign.
    campaignId: post.campaignId,
    // The post's published URL is the canonical URL; mirror it into links.url
    // the same way createContent seeds it so cross-link rewriting matches.
    links: { url: post.url },
    ids: {},
    gsi1pk: `TENANT#${tenantId}#CONTENT`,
    gsi1sk: `${createdAt}#${contentId}`,
    createdAt,
    updatedAt,
  };

  if (post.url !== undefined) item.canonicalUrl = post.url;
  if (post.platform !== undefined) item.platform = post.platform;
  // Campaign posts carry no title; surface `notes` as the description so the
  // operator note (when present) isn't lost.
  if (post.notes !== undefined) item.description = post.notes;
  // Carry the live analytics map + its provenance, when present, so dual-read
  // sees the latest numbers on the root alongside the per-day STATS children.
  if (post.analytics !== undefined) item.analytics = post.analytics;
  if (post.lastFetched !== undefined) item.lastFetched = post.lastFetched;
  if (post.capturedAt !== undefined) item.capturedAt = post.capturedAt;

  return item;
}

// ---------------------------------------------------------------------
// Pure transform: a *Snapshot row -> a Content STATS child item.
//
// Snapshot rows (entity ContentPostSnapshot | SocialPostSnapshot) live at
// sk = {CONTENTPOST|SOCIALPOST}#{postId}#SNAPSHOT#{YYYY-MM-DD} and carry
// { snapshotDate, metrics, capturedAt, recordedAt }. They map onto a Content
// STATS child at sk = CONTENT#{contentId}#STATS#{platform}#{date}, mirroring
// the attribute shape domain putStatsSnapshot produces (entity ContentStats).
//
//   platform: taken from the post (the snapshot row itself carries none).
//   date:     the snapshot's own snapshotDate (the YYYY-MM-DD already embedded
//             in its sk); falls back to parsing it out of the sk if absent.
// Returns null when no usable date can be derived.
// ---------------------------------------------------------------------
export function snapshotToStatsItem(snapshot, post, kind, tenantId) {
  const contentId = contentIdFor(kind, post.postId);
  const platform = post.platform;
  const date = snapshot.snapshotDate ?? snapshotDateFromSk(snapshot.sk);
  if (!date) return null;

  const item = {
    ...statsKey(tenantId, contentId, platform, date),
    entity: "ContentStats",
    tenantId,
    contentId,
    platform,
    date,
    // Carry the captured metrics + when the client observed them. Preserve the
    // snapshot's own capturedAt (NOT "now") so the migrated series keeps its
    // real timeline.
    metrics: snapshot.metrics,
    capturedAt: snapshot.capturedAt ?? snapshot.recordedAt ?? date,
  };
  if (snapshot.recordedAt !== undefined) item.recordedAt = snapshot.recordedAt;

  return item;
}

// Pulls the YYYY-MM-DD out of a snapshot sk like
// "CONTENTPOST#{postId}#SNAPSHOT#2026-06-25" as a fallback when snapshotDate is
// missing from the row.
function snapshotDateFromSk(sk) {
  if (typeof sk !== "string") return null;
  const marker = "#SNAPSHOT#";
  const idx = sk.lastIndexOf(marker);
  if (idx === -1) return null;
  const date = sk.slice(idx + marker.length);
  return /^\d{4}-\d{2}-\d{2}$/.test(date) ? date : null;
}

// Maps a post root entity name to its snapshot child entity name.
const SNAPSHOT_ENTITY = {
  ContentPost: "ContentPostSnapshot",
  SocialPost: "SocialPostSnapshot",
};

// ---------------------------------------------------------------------

const client = new DynamoDBClient({ region: args.region });
const tenantId = args.tenant;

let contentPostsFound = 0;
let socialPostsFound = 0;
let contentRows = 0;
let statsChildren = 0;
let skippedSnapshots = 0;
const samples = [];

// One-shot Scan filtered to the four campaign-post entities: the two post
// roots and their two snapshot children. Snapshots are collected keyed by
// postId so each post's STATS children can be derived alongside its root —
// exactly how the blog script groups crosspost copies under their blog.
const snapshotsByPostId = new Map(); // postId -> [snapshot, ...]
const postRoots = []; // { post, kind }

const paginator = paginateScan(
  { client, pageSize: 100 },
  {
    TableName: TABLE_NAME,
    FilterExpression:
      "#entity = :contentPost OR #entity = :socialPost OR #entity = :contentSnap OR #entity = :socialSnap",
    ExpressionAttributeNames: { "#entity": "entity" },
    ExpressionAttributeValues: {
      ":contentPost": { S: "ContentPost" },
      ":socialPost": { S: "SocialPost" },
      ":contentSnap": { S: SNAPSHOT_ENTITY.ContentPost },
      ":socialSnap": { S: SNAPSHOT_ENTITY.SocialPost },
    },
  },
);

for await (const page of paginator) {
  for (const rawItem of page.Items ?? []) {
    const row = unmarshall(rawItem);
    if (row.entity === "ContentPost" || row.entity === "SocialPost") {
      postRoots.push({ post: row, kind: row.entity });
    } else if (row.entity === SNAPSHOT_ENTITY.ContentPost || row.entity === SNAPSHOT_ENTITY.SocialPost) {
      const list = snapshotsByPostId.get(row.postId) ?? [];
      list.push(row);
      snapshotsByPostId.set(row.postId, list);
    }
  }
}

for (const { post, kind } of postRoots) {
  if (kind === "SocialPost") socialPostsFound += 1;
  else contentPostsFound += 1;

  if (!post.postId || !post.createdAt) {
    console.warn(
      `Skipping malformed ${kind} (missing postId/createdAt): pk=${post.pk} sk=${post.sk}`,
    );
    continue;
  }

  const contentItem = postToContentItem(post, kind, tenantId);
  const snapshots = snapshotsByPostId.get(post.postId) ?? [];
  const statsItems = [];
  for (const snapshot of snapshots) {
    // A snapshot is only this post's if it shares the post's kind. (postIds are
    // unique across kinds, but the kind check is cheap insurance.)
    if (snapshot.entity !== SNAPSHOT_ENTITY[kind]) continue;
    const statsItem = snapshotToStatsItem(snapshot, post, kind, tenantId);
    if (statsItem) {
      statsItems.push(statsItem);
    } else {
      skippedSnapshots += 1;
    }
  }

  if (samples.length < 3) {
    samples.push({ contentItem, statsItems });
  }

  if (!args.apply) {
    console.log(
      `[dry-run] would write Content ${contentItem.tenantId}/${contentItem.contentId} ` +
        `(type=${contentItem.type}, source=${contentItem.source}, platform=${contentItem.platform ?? "(none)"}) ` +
        `from ${kind} ${post.postId} + ${statsItems.length} STATS child(ren)`,
    );
    contentRows += 1;
    statsChildren += statsItems.length;
    continue;
  }

  // Deterministic overwrite of the Content root (no attribute_not_exists guard
  // so re-runs succeed).
  await ddb.send(new PutCommand({ TableName: TABLE_NAME, Item: contentItem }));
  contentRows += 1;
  console.log(`Wrote Content ${contentItem.tenantId}/${contentItem.contentId} (from ${kind})`);

  for (const statsItem of statsItems) {
    await ddb.send(new PutCommand({ TableName: TABLE_NAME, Item: statsItem }));
    statsChildren += 1;
    console.log(`  Wrote STATS ${statsItem.platform}/${statsItem.date}`);
  }
}

// Sample dump (first few transforms) so a dry-run shows the exact shape.
if (samples.length > 0) {
  console.log("---");
  console.log("Sample transform(s):");
  console.log(JSON.stringify(samples, null, 2));
}

console.log("---");
console.log(`Tenant (owner):     ${tenantId}`);
console.log(`Content posts found: ${contentPostsFound}`);
console.log(`Social posts found:  ${socialPostsFound}`);
console.log(`Content rows:       ${contentRows}`);
console.log(`Stats children:     ${statsChildren}`);
console.log(`Skipped snapshots (no date): ${skippedSnapshots}`);
if (!args.apply) {
  console.log("(dry-run; use --apply to write)");
}

function parseArgs(argv) {
  const out = { region: "us-east-1", apply: false, help: false };
  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (arg === "--apply") out.apply = true;
    else if (arg === "--help" || arg === "-h") out.help = true;
    else if (arg === "--table") out.table = argv[++i];
    else if (arg === "--tenant") out.tenant = argv[++i];
    else if (arg === "--region") out.region = argv[++i];
  }
  return out;
}
